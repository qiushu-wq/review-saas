// ─── 生成回复 ────────────────────────────────────────────────
function generateReply(text, store, product, options = {}) {
  const industry = options.industry || 'catering'
  const tone = options.tone || 'warm'
  const severity = detectSeverity(text)

  // 从模板中选取，找不到则降级兜底
  const templates = TEMPLATES[industry]?.[tone]?.[severity]
  let comfort, verify, compensate

  if (templates) {
    comfort = pick(templates.comfort)
    verify = pick(templates.verify)
    compensate = pick(templates.compensate)
  } else {
    comfort = '感谢您的反馈，我们会认真处理。'
    verify = '我们已记录您说的问题，将尽快核实。'
    compensate = '我们将尽快联系您处理，请保持联系方式畅通。'
  }

  const storeName = store || '本店'
  const fullText = `【情绪安抚】\n${comfort}\n\n【原因核实】\n${verify}\n\n【补偿方案】\n${compensate}\n\n—— ${storeName}`

  return { steps: { comfort, verify, compensate }, severity, fullText }
}

